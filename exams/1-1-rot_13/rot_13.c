#include <unistd.h>

int		main(int argc, char *argv[])
{
	int		i;

	i = 0;
	if (argc == 2)
	{
		while (argv[1][i])
		{
			if (argv[1][i] >= 'a' && argv[1][i] <= 'z')
				argv[1][i] += (argv[1][i] + 13 <= 'z') ? 13 : -13;
			else if (argv[1][i] >= 'A' && argv[1][i] <= 'Z')
				argv[1][i] += (argv[1][i] + 13 <= 'Z') ? 13 : -13;
			i++;
		}
		i = 0;
		while (argv[1][i])
			write(1, &argv[1][i++], 1);
	}
	write(1, "\n", 1);
	return (0);
}
