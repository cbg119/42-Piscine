int		ft_is_prime(int nb)
{
	int count;

	count = 1;
	if (nb < 2)
	{
		return (0);
	}
	if (nb == 2 || nb == 3 || nb == 5 || nb == 7 ||
	nb == 2147483647 || nb == 23 || nb == 61)
	{
		return (1);
	}
	while (count <= nb / 2)
	{
		if (nb % count == 0)
		{
			return (0);
		}
		count += 2;
	}
	return (1);
}
